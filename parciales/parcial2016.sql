CREATE VIEW parcial_2016_ProductosNoOrdenados AS
	SELECT p.* 
	FROM unc_esq_producto.producto p
	WHERE NOT EXISTS (	SELECT 1
						FROM unc_esq_producto.productoxorden
						WHERE id_producto=p.id_producto)
		 AND p.precio_unitario < 1000
;


